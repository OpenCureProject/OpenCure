# Function to parse the VCF file
def parse_vcf(file_path):
    records = []
    with open(file_path, 'r') as file:
        for line in file:
            if not line.startswith('##'):
                if line.startswith('#'):
                    columns = line.strip().split('\t')
                else:
                    records.append(line.strip().split('\t'))
    df = pd.DataFrame(records, columns=columns)
    return df

# Function to extract and write the required records to the output file
def extract_and_write_records(file_path, output_file):
    df = parse_vcf(file_path)
    
    cm_records = df[df['#CHROM'] == contig]
    
    with open(output_file, 'w') as file:
        file.write(header)
        for _, row in cm_records.iterrows():
            line = '\t'.join(row) + '\n'
            file.write(line)
        file.write('end')

# Use the function
